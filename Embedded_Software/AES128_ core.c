void ShiftRows (uint32_t state[4])
{
    uint8_t temp[16];
    for (int i = 0; i < 16; i++){
        temp[i] = (state[i/4] >> ((i%4)*8)) & 0xFF;
    }

    state[0] = (temp[0] <<0) | (temp[5] <<8) | (temp[10] <<16) | (temp[15] << 24);
    state[1] = (temp[4] <<0) | (temp[9] <<8) | (temp[14] <<16) | (temp[3] << 24);
    state[2] = (temp[8] <<0) | (temp[13]<<8) | (temp[2] <<16)  | (temp[7] << 24);
    state[3] = (temp[12] <<0)| (temp[1] <<8) | (temp[6] <<16)  | (temp[11]<< 24);
}
