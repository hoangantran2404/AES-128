void SubBytes(uint32_t state[4])
{
    uint8_t temp[4];
    for (int i=0; i<4; i++){
        temp[0] = (state[i] >> 0) & 0xFF;
        temp[1] = (state[i] >> 8) & 0xFF;
        temp[2] = (state[i] >> 16) & 0xFF;
        temp[3] = (state[i] >> 24) & 0xFF;

        SBox(temp);
        
        state[i] = ((uint32_t) temp[0] << 0) | ((uint32_t)temp[1] << 8) | ((uint32_t)temp[2] << 16) | ((uint32_t)temp[3] << 24);
        
    }
}
void KeyExpansion(const uint32_t key_in[4], uint32_t key_out[44]) 
{
    uint32_t temp[4];

    for (int i=0; i<4; i++){
        key_out[i] = key_in[i];
    }

    for (int i=4; i<44; i++){
        temp[0] = key_out[i-1] >> 0  & 0xFF;
        temp[1] = key_out[i-1] >> 8  & 0xFF;
        temp[2] = key_out[i-1] >> 16 & 0xFF;
        temp[3] = key_out[i-1] >> 24 & 0xFF;

        if (i % 4 == 0){
            RotWord(temp, temp);
            SBox(temp);

            if((i/4) <= 10) {
                temp[0] ^= rcon[i/4];
            }
        }

        uint32_t temp_word =   ((uint32_t)temp[0] << 0) | ((uint32_t)temp[1] << 8) | ((uint32_t)temp[2] << 16)| ((uint32_t)temp[3] << 24);
        key_out[i]    =   key_out[i-4] ^ temp_word;
    }
}
void AES128_Encrypt(const uint32_t plaintext[4], const uint32_t key[4], uint32_t ciphertext[4])
{
    uint32_t state[4];
    uint32_t roundKeys[44];

    KeyExpansion(key, roundKeys);

    AddRoundKey(plaintext, &roundKeys[0], state);

    for (int round=1; round <=9; round++){
        SubBytes    (state);
        ShiftRows   (state);
        MixColumns  (state);
        AddRoundKey (state, &roundKeys[round*4], state);
    }

    SubBytes    (state);
    ShiftRows   (state);
    AddRoundKey (state, &roundKeys[40], ciphertext);
}
