void RotWord(uint32_t data_in[4], uint32_t data_out[4])
{
    uint32_t temp = data_in[0];

    data_out[0] = data_in[1];
    data_out[1] = data_in[2];
    data_out[2] = data_in[3];
    data_out[3] = temp;
}
void AddRoundKey(const uint32_t state[4], const uint32_t roundKey[4], uint32_t data_out[4])
{
    data_out[0] = state[0] ^ roundKey[0];
    data_out[1] = state[1] ^ roundKey[1];
    data_out[2] = state[2] ^ roundKey[2];
    data_out[3] = state[3] ^ roundKey[3];
}

 uint8_t GFMult(uint8_t a, uint8_t b)
{
    uint8_t temp_a = a;
    uint8_t      p = 0x00;

    for (int i=0; i<8; i++){
        if((b & 0x01) == 1)
            p ^= temp_a;

        uint8_t hi_bit_set = (temp_a & 0x80); 

        temp_a = temp_a << 1;

        if(hi_bit_set){
            temp_a ^= 0x1b; 
        }
        b = b >> 1;
    }

    return p;
}
void MixColumns(uint32_t word_in[4])
{
    uint32_t temp[4];
    uint8_t  c[4];
    uint8_t  r[4];

    for(int i = 0; i< 4; i++){
        c[0] = ((word_in[i]) >> 0)  & 0xFF;
        c[1] = ((word_in[i]) >> 8)  & 0xFF;
        c[2] = ((word_in[i]) >> 16) & 0xFF;
        c[3] = ((word_in[i]) >> 24) & 0xFF;
    
        r[0] = GFMult(c[0], 0x02) ^ GFMult(c[1], 0x03) ^ c[2] ^ c[3];
        r[1] = c[0] ^ GFMult(c[1], 0x02) ^ GFMult(c[2], 0x03) ^ c[3];
        r[2] = c[0] ^ c[1] ^ GFMult(c[2],0x02) ^ GFMult(c[3],0x03);
        r[3] = GFMult(c[0],0x03) ^ c[1] ^ c[2] ^ GFMult(c[3],0x02);
        
        temp[i] = (r[0] << 0) | (r[1] << 8) | (r[2] << 16) | (r[3] << 24);
    }

    for (int i=0; i<4; i++){
        word_in[i] = temp[i];
    }
}

